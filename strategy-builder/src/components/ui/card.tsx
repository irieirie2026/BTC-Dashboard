const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={className ? `card ${className}` : "card"}
    {...props}
  />
));
Card.displayName = "Card";

